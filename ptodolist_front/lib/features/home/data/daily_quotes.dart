/// 홈 명언 카드에 표시할 문구 풀.
/// 날짜 단위 결정적(deterministic)으로 회전 — 같은 날 앱을 여러 번 켜도 동일 문구,
/// 다음 날이 되면 다음 문구로 자동 전환.
class DailyQuotes {
  static const List<String> _quotes = [
    '"작은 습관이 큰 변화를 만듭니다.\n오늘도 한 걸음 더 나아가세요."',
    '"천 리 길도 한 걸음부터.\n오늘 그 한 걸음을 내딛어 보세요."',
    '"완벽하지 않아도 괜찮아요.\n중요한 건 꾸준히 하는 것이에요."',
    '"오늘의 나는 어제의 나보다\n조금 더 나아졌습니다."',
    '"성공은 작은 노력들이 반복될 때\n찾아옵니다."',
    '"하루를 아끼는 사람이\n인생을 아낀다."',
    '"시작하는 것이 언제나 가장 어렵죠.\n이미 시작한 당신은 대단합니다."',
    '"오늘 심은 씨앗이\n내일의 습관이 됩니다."',
    '"넘어져도 괜찮아요.\n다시 일어나면 됩니다."',
    '"당신의 속도가 가장 빠른 속도입니다.\n스스로를 믿으세요."',
    '"매일 조금씩, 꾸준히.\n그게 가장 강한 힘입니다."',
    '"오늘 하루도 수고했어요.\n내일도 함께해요."',
    '"작은 성취도 충분히 자랑할 만해요."',
    '"계획대로 안 돼도 괜찮아요.\n다시 시작하면 됩니다."',
    '"어제와 다른 오늘을 만드는 건\n당신의 오늘 한 걸음입니다."',
  ];

  /// 주어진 날짜에 해당하는 명언을 반환 (같은 날은 항상 같은 문구).
  static String forDate(DateTime date) {
    final dayOfYear =
        date.difference(DateTime(date.year, 1, 1)).inDays;
    final index = dayOfYear.abs() % _quotes.length;
    return _quotes[index];
  }

  static int get count => _quotes.length;
}
